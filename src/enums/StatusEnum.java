package enums;

/**
 * Enum representing the status of various entities such as requests or tasks.
 */
public enum StatusEnum {
    /**
     * Indicates that the request or task is pending and awaiting approval or action.
     */
    PENDING,

    /**
     * Indicates that the request or task has been approved.
     */
    APPROVED,

    /**
     * Indicates that the request or task has been rejected.
     */
    REJECTED
}
